/**
 * AND (&): The AND operator sets a bit to 1 if both corresponding bits of the operands are 1; otherwise, it sets the bit to 0.

OR (|): The OR operator sets a bit to 1 if either of the corresponding bits of the operands is 1; otherwise, it sets the bit to 0.

XOR (^): The XOR operator sets a bit to 1 if the corresponding bits of the operands are different; otherwise, it sets the bit to 0.

NOT (~): The NOT operator flips all the bits of its operand (1s become 0s, and vice versa).

Left Shift (<<): The left shift operator shifts the bits of its operand to the left by a specified number of positions, padding with zeros on the right.

Right Shift (>>): The right shift operator shifts the bits of its operand to the right by a specified number of positions, filling with zeros on the left for unsigned numbers or the sign bit for signed numbers.
 * 
 */